# 예시 1 변수 선언 및 값 할당
name = "철수"  # 문자열
age = 25       # 정수
height = 175.5 # 실수
is_student = True  # 불리언

# 변수 값 출력
print(name)       # 출력: 철수
print(age)        # 출력: 25
print(height)     # 출력: 175.5
print(is_student) # 출력: True

# 변수 값 변경
age = 26
print(age)        # 출력: 26

# 여러 변수 한 번에 선언
x, y, z = 10, 20, 30
print(x, y, z)    # 출력: 10 20 30

# 예시 2
# 간단한 계산
price = 1000
quantity = 3
total = price * quantity
print(f"총 가격: {total}원")  # 출력: 총 가격: 3000원